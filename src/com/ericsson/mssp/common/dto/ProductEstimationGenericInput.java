package com.ericsson.mssp.common.dto;

/**
 * @author EGI
 * 
 */
public class ProductEstimationGenericInput extends BaseDTO {

	private Integer opportunityID;
	private String serviceFunctionCode;

	public ProductEstimationGenericInput() {
		super();
	}

	public ProductEstimationGenericInput(Integer opportunityID,
			String serviceFunctionCode) {
		super();
		this.opportunityID = opportunityID;
		this.serviceFunctionCode = serviceFunctionCode;
	}

	public Integer getOpportunityID() {
		return opportunityID;
	}

	public void setOpportunityID(Integer opportunityID) {
		this.opportunityID = opportunityID;
	}

	public String getServiceFunctionCode() {
		return serviceFunctionCode;
	}

	public void setServiceFunctionCode(String serviceFunctionCode) {
		this.serviceFunctionCode = serviceFunctionCode;
	}
}
