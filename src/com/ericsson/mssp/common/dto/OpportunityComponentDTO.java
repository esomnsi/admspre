package com.ericsson.mssp.common.dto;

import com.ericsson.mssp.common.entity.Component;
import com.ericsson.mssp.common.entity.Opportunity;

public class OpportunityComponentDTO {

	private Integer opportunityComponentID;
	private Opportunity opportunity;
	private Component component;

	public Integer getOpportunityComponentID() {
		return opportunityComponentID;
	}

	public void setOpportunityComponentID(Integer opportunityComponentID) {
		this.opportunityComponentID = opportunityComponentID;
	}

	public Opportunity getOpportunity() {
		return opportunity;
	}

	public void setOpportunity(Opportunity opportunity) {
		this.opportunity = opportunity;
	}

	public Component getComponent() {
		return component;
	}

	public void setComponent(Component component) {
		this.component = component;
	}

}
