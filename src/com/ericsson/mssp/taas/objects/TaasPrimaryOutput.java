package com.ericsson.mssp.taas.objects;

public class TaasPrimaryOutput 
{
	private int numRelease;
	private long numManualTC;
	private long numAutoTC;
	private double manualTestEff;
	private double autoDevEff;
	
	public int getNumRelease() {
		return numRelease;
	}
	public void setNumRelease(int numRelease) {
		this.numRelease = numRelease;
	}
	public long getNumManualTC() {
		return numManualTC;
	}
	public void setNumManualTC(long numManualTC) {
		this.numManualTC = numManualTC;
	}
	public long getNumAutoTC() {
		return numAutoTC;
	}
	public void setNumAutoTC(long numAutoTC) {
		this.numAutoTC = numAutoTC;
	}
	public double getManualTestEff() {
		return manualTestEff;
	}
	public void setManualTestEff(double manualTestEff) {
		this.manualTestEff = manualTestEff;
	}
	public double getAutoDevEff() {
		return autoDevEff;
	}
	public void setAutoDevEff(double autoDevEff) {
		this.autoDevEff = autoDevEff;
	}
}
