package com.ericsson.mssp.common.dto;

public class JobRoleDTO {

	private Integer jobRoleId;
	private String usedByFA;
	private String HRMSObject;
	private String role;
	private String jobRoleFamily;
	private String jobRoleFamilyNumber;
	private Integer CCMFlag;
	private String selected;
	
	
	public String getSelected() {
		return selected;
	}
	public void setSelected(String selected) {
		this.selected = selected;
	}
	public String getUsedByFA() {
		return usedByFA;
	}
	public void setUsedByFA(String usedByFA) {
		this.usedByFA = usedByFA;
	}
	public String getHRMSObject() {
		return HRMSObject;
	}
	public void setHRMSObject(String hRMSObject) {
		HRMSObject = hRMSObject;
	}
	public String getJobRoleFamily() {
		return jobRoleFamily;
	}
	public void setJobRoleFamily(String jobRoleFamily) {
		this.jobRoleFamily = jobRoleFamily;
	}
	public String getJobRoleFamilyNumber() {
		return jobRoleFamilyNumber;
	}
	public void setJobRoleFamilyNumber(String jobRoleFamilyNumber) {
		this.jobRoleFamilyNumber = jobRoleFamilyNumber;
	}
	public Integer getCCMFlag() {
		return CCMFlag;
	}
	public void setCCMFlag(Integer cCMFlag) {
		this.CCMFlag = cCMFlag;
	}
	public Integer getJobRoleId() {
		return jobRoleId;
	}
	public void setJobRoleId(Integer jobRoleId) {
		this.jobRoleId = jobRoleId;
	}
	public String getRole() {
		return role;
	}
	public void setRole(String role) {
		this.role = role;
	}
}
