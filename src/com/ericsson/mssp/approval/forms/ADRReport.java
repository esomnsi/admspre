package com.ericsson.mssp.approval.forms;

import java.util.List;

import com.ericsson.mssp.common.entity.SolutionADR;

public class ADRReport {
	private List<SolutionADR> assumptionList;
	private List<SolutionADR> dependencyList;
	private List<SolutionADR> riskList;
	public List<SolutionADR> getAssumptionList() {
		return assumptionList;
	}
	public void setAssumptionList(List<SolutionADR> assumptionList) {
		this.assumptionList = assumptionList;
	}
	public List<SolutionADR> getDependencyList() {
		return dependencyList;
	}
	public void setDependencyList(List<SolutionADR> dependencyList) {
		this.dependencyList = dependencyList;
	}
	public List<SolutionADR> getRiskList() {
		return riskList;
	}
	public void setRiskList(List<SolutionADR> riskList) {
		this.riskList = riskList;
	}
	
}
