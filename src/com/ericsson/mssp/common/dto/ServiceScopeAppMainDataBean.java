package com.ericsson.mssp.common.dto;

public class ServiceScopeAppMainDataBean {
	
	private Integer solutionId;
	private Integer oppScopeId;
	private String servScopeName;
	private Double totalBaseFTE;
	private Double totalBaseHours;
	
	public Integer getSolutionId() {
		return solutionId;
	}
	public void setSolutionId(Integer solutionId) {
		this.solutionId = solutionId;
	}
	public Integer getOppScopeId() {
		return oppScopeId;
	}
	public void setOppScopeId(Integer oppScopeId) {
		this.oppScopeId = oppScopeId;
	}
	public String getServScopeName() {
		return servScopeName;
	}
	public void setServScopeName(String servScopeName) {
		this.servScopeName = servScopeName;
	}
	public Double getTotalBaseFTE() {
		return totalBaseFTE;
	}
	public void setTotalBaseFTE(Double totalBaseFTE) {
		this.totalBaseFTE = totalBaseFTE;
	}
	public Double getTotalBaseHours() {
		return totalBaseHours;
	}
	public void setTotalBaseHours(Double totalBaseHours) {
		this.totalBaseHours = totalBaseHours;
	}
	
	

}
