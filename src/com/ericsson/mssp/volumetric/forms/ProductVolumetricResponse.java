package com.ericsson.mssp.volumetric.forms;

import java.util.List;

import com.ericsson.mssp.common.dto.ProductVolumetricDTO;

public class ProductVolumetricResponse {

	/**
	 * Product Estimation Activities - actual data to be displayed in UI
	 */
	private List<ProductVolumetricDTO> productEstimationActivityList;
	/**
	 * Current page of the query
	 */
	private String page;

	/**
	 * Total pages for the query
	 */
	private String total;

	/**
	 * Total number of records for the query
	 */
	private String records;

	public List<ProductVolumetricDTO> getProductEstimationActivityList() {
		return productEstimationActivityList;
	}

	public void setProductEstimationActivityList(
			List<ProductVolumetricDTO> productEstimationActivityList) {
		this.productEstimationActivityList = productEstimationActivityList;
	}

	public String getPage() {
		return page;
	}

	public void setPage(String page) {
		this.page = page;
	}

	public String getTotal() {
		return total;
	}

	public void setTotal(String total) {
		this.total = total;
	}

	public String getRecords() {
		return records;
	}

	public void setRecords(String records) {
		this.records = records;
	}
}
