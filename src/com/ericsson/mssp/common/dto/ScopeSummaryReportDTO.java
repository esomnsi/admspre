package com.ericsson.mssp.common.dto;

public class ScopeSummaryReportDTO {
	private String department;
	private String category;
	private double avgUtil;
	private double totalOutsourcedHrs;
	public String getDepartment() {
		return department;
	}
	public void setDepartment(String department) {
		this.department = department;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public double getAvgUtil() {
		return avgUtil;
	}
	public void setAvgUtil(double avgUtil) {
		this.avgUtil = avgUtil;
	}
	public double getTotalOutsourcedHrs() {
		return totalOutsourcedHrs;
	}
	public void setTotalOutsourcedHrs(double totalOutsourcedHrs) {
		this.totalOutsourcedHrs = totalOutsourcedHrs;
	}
}

