package com.ericsson.mssp.common.dto;

import java.util.Date;



/**
 * @author egaivij
 *
 */
public class ProductivityLeverDTO {

	private Integer productivityLeverId;
	private SolutionLeverDTO solutionLeverDTO;
	private OpportunityScopeDTO opportunityScopeDTO;
	private Date monthYear;
	
	private Float productivityPC;
	
	private Float calculatedFte;

	/**
	 * @return the productivityLeverId
	 */
	public Integer getProductivityLeverId() {
		return productivityLeverId;
	}

	/**
	 * @param productivityLeverId the productivityLeverId to set
	 */
	public void setProductivityLeverId(Integer productivityLeverId) {
		this.productivityLeverId = productivityLeverId;
	}

	
	

	/**
	 * @return the solutionLeverDTO
	 */
	public SolutionLeverDTO getSolutionLeverDTO() {
		return solutionLeverDTO;
	}

	/**
	 * @param solutionLeverDTO the solutionLeverDTO to set
	 */
	public void setSolutionLeverDTO(SolutionLeverDTO solutionLeverDTO) {
		this.solutionLeverDTO = solutionLeverDTO;
	}

	/**
	 * @return the opportunityScopeDTO
	 */
	public OpportunityScopeDTO getOpportunityScopeDTO() {
		return opportunityScopeDTO;
	}

	/**
	 * @param opportunityScopeDTO the opportunityScopeDTO to set
	 */
	public void setOpportunityScopeDTO(OpportunityScopeDTO opportunityScopeDTO) {
		this.opportunityScopeDTO = opportunityScopeDTO;
	}

	/**
	 * @return the monthYear
	 */
	public Date getMonthYear() {
		return monthYear;
	}

	/**
	 * @param monthYear the monthYear to set
	 */
	public void setMonthYear(Date monthYear) {
		this.monthYear = monthYear;
	}

	/**
	 * @return the productivityPC
	 */
	public Float getProductivityPC() {
		return productivityPC;
	}

	/**
	 * @param productivityPC the productivityPC to set
	 */
	public void setProductivityPC(Float productivityPC) {
		this.productivityPC = productivityPC;
	}

	/**
	 * @return the calculatedFte
	 */
	public Float getCalculatedFte() {
		return calculatedFte;
	}

	/**
	 * @param calculatedFte the calculatedFte to set
	 */
	public void setCalculatedFte(Float calculatedFte) {
		this.calculatedFte = calculatedFte;
	}
	

}
