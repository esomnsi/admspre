package com.ericsson.mssp.approval.forms;

public class ServiceFTEReport {
	private String serviceName;
	private String operationWindow;
	private Double fte;
	private String skills;
	private int serviceScopeId;
	private int opportunityScopeId;
	
	public String getServiceName() {
		return serviceName;
	}
	public void setServiceName(String serviceName) {
		this.serviceName = serviceName;
	}
	public String getOperationWindow() {
		return operationWindow;
	}
	public void setOperationWindow(String operationWindow) {
		this.operationWindow = operationWindow;
	}
	public Double getFte() {
		return fte;
	}
	public void setFte(Double fte) {
		this.fte = fte;
	}
	public String getSkills() {
		return skills;
	}
	public void setSkills(String skills) {
		this.skills = skills;
	}
	public int getServiceScopeId() {
		return serviceScopeId;
	}
	public void setServiceScopeId(int serviceScopeId) {
		this.serviceScopeId = serviceScopeId;
	}
	public int getOpportunityScopeId() {
		return opportunityScopeId;
	}
	public void setOpportunityScopeId(int opportunityScopeId) {
		this.opportunityScopeId = opportunityScopeId;
	}
	
}
